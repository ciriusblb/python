def sumar(a,b):
    print("El resultado de la suma es: ", a+b)
def restar(a,b):
    print("El resultado de la resta es: ", a-b)
def multiplicar(a,b):
    print("El resultado de la mutiplicacion es: ", a*b)
def dividir(a,b):
    print("El resultado de la division es: ", a/b)
def potencia(a,b):
    print("El resultado de la potencia es: ", a**b)
def redondear(a):
    print("El resultado del redondeo es: ", round(a))